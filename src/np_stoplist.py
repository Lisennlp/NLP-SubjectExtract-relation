def is_stop_np(np, pos):
    if pos.startswith('np'):
        return False #, None
    if pos.endswith('a+u') and any(np.endswith(s) for s in stop_nps['a+u']):
        return True #, [s for s in in stop_nps['a+u'] if np.endswith(s)][0]
    if pos.endswith('+d') and any(np.endswith(s) for s in stop_nps['d']):
        return True #, [s for s in stop_nps['d'] if np.endswith(s)][0]
    if '+u' in pos and not pos.endswith('+u+n') and not pos.startswith('a+u') and not pos.startswith('v+u') and '的' in np:
        pos = 'x+u'
    if not any(pos == s or pos.startswith(s + '+') for s in stop_nps):
        pos = 'others'
    for pos0 in stop_nps:
        if (pos == pos0 or pos.startswith(pos0 + '+')) and any(np.startswith(s) for s in stop_nps[pos0]):
            return True #, [s for s in stop_nps[pos0] if np.startswith(s)][0]
    return False #, None
